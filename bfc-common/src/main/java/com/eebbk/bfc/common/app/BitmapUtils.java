    /**
     * 获取bitmap
     *
     * @param file 文件
     * @return bitmap, 获取失败, 返回null
     */
    public static Bitmap getBitmap(@NonNull File file) {
        InputStream is = null;
        try {
            is = new BufferedInputStream(new FileInputStream(file));
            return BitmapFactory.decodeStream(is);
        } catch (FileNotFoundException e) {
            LogInner.w(TAG, e, "获取bitmap失败, 请检查传入的文件是否存在");
            return null;
        } finally {
            FileUtils.closeIO(is);
        }
    }

    /**
     * 获取bitmap, 会使用传入的宽度和高度, 对bitmap进行压缩
     *
     * @param file      文件
     * @param maxWidth  最大宽度
     * @param maxHeight 最大高度
     * @return bitmap, 获取失败, 返回null
     */
    public static Bitmap getBitmap(@NonNull File file, int maxWidth, int maxHeight) {
        InputStream is = null;
        try {
            BitmapFactory.Options options = new BitmapFactory.Options();
            options.inJustDecodeBounds = true;
            is = new BufferedInputStream(new FileInputStream(file));
            BitmapFactory.decodeStream(is, null, options);
            options.inSampleSize = calculateInSampleSize(options, maxWidth, maxHeight);
            options.inJustDecodeBounds = false;
            return BitmapFactory.decodeStream(is, null, options);
        } catch (FileNotFoundException e) {
            LogInner.w(TAG, e, "获取bitmap失败, 请检查传入的文件是否存在");
            return null;
        } finally {
            FileUtils.closeIO(is);
        }
    }

